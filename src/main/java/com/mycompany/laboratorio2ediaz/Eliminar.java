    /**
     * @param args the command line arguments
     */
    public void mostrarUsuarios() {
    // Obtenemos el modelo de tu tabla naranja
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0); 

    // Asegúrate de que "login" sea el nombre exacto de tu clase donde están las listas
    for (int i = 0; i < com.mycompany.laboratorio2ediaz.login.listaUsuarios.size(); i++) {
        Object[] fila = new Object[2];
        fila[0] = com.mycompany.laboratorio2ediaz.login.listaUsuarios.get(i);
        fila[1] = com.mycompany.laboratorio2ediaz.login.listaEstados.get(i);
        modelo.addRow(fila);
    }
}
