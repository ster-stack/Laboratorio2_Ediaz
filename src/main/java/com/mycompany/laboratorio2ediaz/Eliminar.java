    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
                // Dentro del evento del botón Inactivar
int fila = jTable1.getSelectedRow();
if (fila != -1) {
    // Obtenemos el nombre del usuario de la columna 0
    String userSeleccionado = jTable1.getValueAt(fila, 0).toString();
    
    // Cambiamos el estado en el ArrayList del login
    login.listaEstados.set(fila, "INACTIVO");
    
    // Actualizamos la tabla para ver el cambio
    mostrarUsuarios(); 
    javax.swing.JOptionPane.showMessageDialog(this, "Usuario " + userSeleccionado + " inactivado.");
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un usuario de la tabla.");
}
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Confirmacion admin = new Confirmacion();
        admin.setVisible(true);
        this. dispose();
        int fila = jTable1.getSelectedRow();
if (fila != -1) {
    int confirmar = javax.swing.JOptionPane.showConfirmDialog(this, "¿Seguro que quieres eliminar este usuario?");
    
    if (confirmar == 0) { // Si el admin dice que SÍ
        login.listaUsuarios.remove(fila);
        login.listaPasswords.remove(fila);
        login.listaEstados.remove(fila);
        
        mostrarUsuarios(); // Refresca la tabla
        javax.swing.JOptionPane.showMessageDialog(this, "Usuario borrado del sistema.");
    }
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Selecciona a quién quieres eliminar.");
}

    }//GEN-LAST:event_jButton4ActionPerformed

